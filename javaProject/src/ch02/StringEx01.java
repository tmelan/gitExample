package ch02;

public class StringEx01 {

	public static void main(String[] args) {
		// string은 문자열을 다루는 데이터타입, 단문자는 char
		String name = "홍길동"; // 문자열은 큰따옴표, 단문자는 작은따옴표를 사용 
		char a = 'b'; //단문자
		System.out.println("당신의 이름은 " + name + "입니다.");
		System.out.println("단문자 : " + a );
		

	}

}
